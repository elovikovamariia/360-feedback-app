/** Примеры формулировок для анкеты 360° (самооценка и оценка сотрудника). */

function firstName(fullName: string): string {
  const t = fullName.trim();
  if (!t) return "коллега";
  return t.split(/\s+/)[0] ?? t;
}

export function demoCompetencyComment(args: {
  relationship: string;
  competencyTitle: string;
  score: number;
  revieweeName: string;
  reviewerName: string;
}): string {
  const ct = args.competencyTitle;
  const s = Math.min(5, Math.max(1, args.score));
  const who = firstName(args.revieweeName);
  const me = firstName(args.reviewerName);

  if (args.relationship === "SELF") {
    const lines: Record<number, string> = {
      5: `По «${ct}» ставлю ${s}: стабильно получается держать планку и помогать команде; хочу закрепить это в привычках, а не «на аврал».`,
      4: `По «${ct}» — ${s}: в большинстве ситуаций чувствую уверенность; иногда не хватает времени заранее синхронизировать ожидания.`,
      3: `«${ct}» на уровне ${s}: соответствую роли, но вижу запас по системности — хочу меньше тушить пожары и больше предотвращать.`,
      2: `«${ct}» — ${s}: честно признаю просадку в отдельных кейсах; беру это в фокус развития на квартал.`,
      1: `По «${ct}» — ${s}: пока не дотягиваю до ожидаемого уровня; прошу обратную связь у руководителя и закреплю маленькие шаги.`,
    };
    return lines[s] ?? lines[3]!;
  }

  if (args.relationship === "MANAGER") {
    const lines: Record<number, string> = {
      5: `По «${ct}» — ${s}: ${who} стабильно показывает сильный уровень; опираюсь на него в приоритетных темах.`,
      4: `«${ct}» — ${s}: у ${who} всё уверенно; иногда стоит раньше сигнализировать о рисках по срокам, чтобы не копить напряжение.`,
      3: `По «${ct}» — ${s}: база хорошая; договоримся о 1–2 наблюдаемых индикаторах улучшения до следующего чек-ина.`,
      2: `«${ct}» — ${s}: вижу зазор с ожиданиями роли; готов поддержать план развития и промежуточные точки контроля.`,
      1: `По «${ct}» — ${s}: сейчас недостаточно для роли; важно не оставлять это без разговора 1:1 и конкретных примеров.`,
    };
    return lines[s] ?? lines[3]!;
  }

  // PEER, SUBORDINATE и прочие — оцениваем сотрудника (reviewee), не себя
  const lines: Record<number, string> = {
    5: `По «${ct}» — ${s}: с ${who} удобно вести совместные задачи; надежно и по сути. — ${me}`,
    4: `«${ct}» — ${s}: ${who} сильна в этой плоскости; из пожеланий — чуть больше прозрачности статуса для зависимых сторон.`,
    3: `По «${ct}» — ${s}: нормальный уровень для контекста команды; есть куда подтянуть детали и предсказуемость сроков.`,
    2: `«${ct}» — ${s}: у ${who} здесь заметен разрыв с ожиданиями; лучше разобрать на примерах без общих фраз.`,
    1: `По «${ct}» — ${s}: сейчас это зона риска для ${who}; важно дать поддержку и ясные критерии, что изменится.`,
  };
  return lines[s] ?? lines[3]!;
}

export function demoGeneralComment(args: {
  relationship: string;
  revieweeName: string;
  reviewerName: string;
}): string {
  const who = args.revieweeName.trim();
  const first = firstName(who);
  const me = firstName(args.reviewerName);

  if (args.relationship === "SELF") {
    return (
      `${first}: за полугодие сильнее всего чувствую вклад в кросс-командные задачи и доверие заказчиков. ` +
      `Зона развития — жёстче обозначать границы при перегрузе и раньше просить ресурс. Готов(а) обсудить это на 1:1 и обновить ИПР.`
    );
  }
  if (args.relationship === "MANAGER") {
    return (
      `Как руководитель вижу ${who} как надёжного исполнителя с высокой планкой качества. Коллеги ценят готовность помочь; ` +
      `мой запрос — заранее сигнализировать о рисках срыва сроков и не тащить всё в одиночку. Предлагаю зафиксировать 2–3 приоритета на квартал.`
    );
  }
  return (
    `Работали с ${first} в одной цепочке задач: по делу, в срок, с вниманием к деталям. Из зоны роста — чуть больше видимости статуса для зависимых команд. ` +
    `Формулировки в персональном отчёте сотрудника обобщаются без указания авторов. — ${me}`
  );
}
