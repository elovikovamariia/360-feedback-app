/**
 * Контекстные подсказки для демо-теста (ориентир: business_analysis_360_feedback_service.md, UC-1…UC-6).
 */

import type { PreviewRoleId } from "./roles";
import { canAccess } from "./roles";

export type DemoContextualHint = {
  /** Короткая метка этапа */
  tag: string;
  /** Одна-две фразы: что сделать на этом экране */
  message: string;
  ctaHref?: string;
  ctaLabel?: string;
};

export function getContextualDemoHint(role: PreviewRoleId, pathname: string): DemoContextualHint | null {
  const p = pathname || "/";

  if (p.startsWith("/survey/")) return null;

  if (p === "/") {
    return {
      tag: "Сценарий демо",
      message:
        "Переключите роль в шапке и пройдите цепочку: HR запускает цикл на компанию → в цикле назначаются самооценка, руководитель и коллеги → респонденты заполняют анкеты → HR смотрит отчёты и заполнение.",
      ctaHref: "/hr",
      ctaLabel: "К циклам (HR)",
    };
  }

  if (p === "/hr") {
    if (role === "hr_admin") {
      return {
        tag: "UC-1 · Запуск цикла",
        message:
          "Создайте цикл на всю компанию (полугодие и даты подставятся сами). Затем откройте карточку цикла — там назначение оцениваемого и респондентов (UC-2).",
      };
    }
    return {
      tag: "Доступ",
      message: "Эта страница только для роли HR. Откройте переключатель «Просмотр как…» в шапке и выберите HR.",
      ctaHref: "/",
      ctaLabel: "Главная",
    };
  }

  if (p.startsWith("/hr/cycles/")) {
    if (role === "hr_admin") {
      return {
        tag: "UC-2 · Респонденты",
        message:
          "Назначьте оцениваемого сотрудника и связки «самооценка», «руководитель», «коллега» — появятся персональные ссылки. Именно так анкета доходит до сотрудника и его окружения.",
      };
    }
    return {
      tag: "Доступ",
      message: "Панель цикла в демо доступна в роли HR.",
      ctaHref: "/",
      ctaLabel: "Главная",
    };
  }

  if (p === "/tasks") {
    if (!canAccess(role, "respondent_tasks")) {
      return {
        tag: "Роль",
        message: "Раздел «Мои анкеты» в демо доступен в ролях Сотрудник, Руководитель, Респондент и HR.",
      };
    }
    return {
      tag: "UC-3 · Анкета",
      message:
        "Страница разделена: сначала блок «Самооценка», затем «Оценка других». Задания подставляются по персоне демо (Анна / Дмитрий). Шкала 1–5 и короткий текст; ответы учитываются в заполнении цикла для HR.",
    };
  }

  if (p === "/reports") {
    if (!canAccess(role, "reports")) {
      return {
        tag: "Доступ",
        message: "Отчёты и сводка по циклам в этом демо доступны в ролях HR, Руководитель или Top Management.",
        ctaHref: "/",
        ctaLabel: "Главная",
      };
    }
    if (role === "manager") {
      return {
        tag: "UC-4 · Контроль",
        message:
          "Сводка по циклам в рамках вашей команды: доля заполненных анкет и быстрый переход к радару по сотруднику.",
      };
    }
    if (role === "executive") {
      return {
        tag: "UC-6 · Сводка",
        message: "Агрегированные показатели по циклам без операционной панели HR — для обзора динамики.",
      };
    }
    return {
      tag: "UC-4 · Статус",
      message:
        "По каждому циклу видно охват и заполнение. Откройте сотрудника — радар по компетенциям и срез «сам / руководитель / коллеги» (UC-6).",
    };
  }

  if (p === "/me") {
    if (!canAccess(role, "own_results")) {
      return {
        tag: "Доступ",
        message: "Страница «Мои результаты» в демо открыта для ролей Сотрудник и Руководитель.",
        ctaHref: "/",
        ctaLabel: "Главная",
      };
    }
    if (role === "manager") {
      return {
        tag: "UC-6 · Ваш профиль",
        message: "Итоги оценки по вам: радар и сравнение групп; сырые комментарии скрыты — доступна AI-сводка на странице результатов.",
      };
    }
    return {
      tag: "UC-6 · Итоги",
      message:
        "Краткий вход к вашим результатам по активному циклу: радар, средние по ролям и AI-обобщение смысла обратной связи.",
    };
  }

  if (p === "/team") {
    if (!canAccess(role, "directory")) {
      return {
        tag: "Доступ",
        message: "Справочник команды в демо доступен в ролях HR и Руководитель.",
        ctaHref: "/",
        ctaLabel: "Главная",
      };
    }
    return {
      tag: "Справочник",
      message: "Список людей в оргструктуре — ориентир при подборе респондентов к циклу (дополнение к UC-2).",
    };
  }

  if (p.startsWith("/results/")) {
    return {
      tag: "UC-5–6 · Результат",
      message:
        "Радар по компетенциям, сравнение самооценки с оценками руководителя и коллег, при необходимости — AI-темы и рекомендации по текстам.",
    };
  }

  return {
    tag: "Демо",
    message: "Выберите раздел в меню или переключите роль, чтобы показать соответствующий сценарий оценки 360°.",
  };
}
